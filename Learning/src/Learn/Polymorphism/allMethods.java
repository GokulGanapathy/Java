package Learn.Polymorphism;

public class allMethods {
	
	public void getvalues()
	{
		System.out.println("this is the method for Empty values");
	}
	public void getvalues(int n,String abc)
	{
		System.out.println("This method has a integer "+n+" and a String "+abc);
	}
	public void getvalues(String str,String str1)
	{
		System.out.println("This method has a String "+str+" and a String "+str1);
	}

}
