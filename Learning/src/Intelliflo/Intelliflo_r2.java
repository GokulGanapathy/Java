package Intelliflo;

public class Intelliflo_r2 {

	public static void main(String[] args) {
		
		int table = 7;
		for(int i=1;i<=10;++i) {
			System.out.println(table +" * "+i+" = "+(table*i));
		}
		
		int tab=8;
		int j=11;
		while(j<=10) {
			System.out.println(tab +" * "+j+" = "+(tab*j));
			j++;
		}
		
		int tab1=9;
		int k=11;
		do {
			System.out.println(tab1 +" * "+k+" = "+(tab1*k));
			k++;
		}while(k<=10);
		
		 
	}

}
